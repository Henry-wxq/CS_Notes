#include <stdio.h>

/*
 * 1. 导入一个文件 studio.h std是一个标准库 io是输入输出 h是头文件
 * 2. int 数据类型是整数类型 如果出现在函数名前面表示函数返回值是整数类型
 * 3. main 程序的主函数，程序有且只有一个主函数，括号里面表示函数的参数，主函数没有参数
 */

int main() {
    // 函数体
    // printf 是stdio.h中系统提供的函数，表示标准输出设备上打印的字符串
    printf("Hello, World!");
    // return 如果出现在其它函数中表示函数结束，出现在main函数中表示程序结束
    // 0 表示函数的返回值，好喝函数的返回值类型一致
    return 0;
}
